import Foundation
import UIKit

extension UIColor {
    class func redBgColor() -> UIColor {
        return UIColor(red: CGFloat(248/255.0), green: CGFloat(134/255.0), blue: CGFloat(130/255.0), alpha: 1.0)
    }
    
    class func greenBgColor() -> UIColor {
        return UIColor(red: CGFloat(131/255.0), green: CGFloat(244/255.0), blue: CGFloat(180/255.0), alpha: 1.0)
    }
}
